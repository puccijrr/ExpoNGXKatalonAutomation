def check_link() {
    KeywordLogger log = new KeywordLogger()

    url_check = (url = WebUI.getUrl())

    if (url_check == GlobalVariable.legacyEdit) {
        KeywordUtil.markPassed('PASS: the correct URL was found after clicking the Edit Event link')

        log.logInfo(url_check)
    } else {
        KeywordUtil.markFailed('FAIL: incorrect url found!!')

        log.logInfo(url_check)
    }
}


