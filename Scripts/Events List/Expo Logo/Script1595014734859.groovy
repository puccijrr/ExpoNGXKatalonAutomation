def check_link() {
	KeywordLogger log = new KeywordLogger()

	url_check = (url = WebUI.getUrl())

	if (url_check == GlobalVariable.events) {
		KeywordUtil.markPassed('PASS: the correct URL was found after clicking the Expo logo icon')

		log.logInfo(url_check)
	} else {
		KeywordUtil.markFailed('FAIL: incorrect url found, logo click action may not have worked!!')

		log.logInfo(url_check)
	}
}


